int		ft_count_words(char *s)
{
	int i;
	int word;

	i = 0;
	word = 0;
	if (!s)
		return (0);
	while (s[i])
	{
		if (s[i] != ' ' && s[i] != '\t')
		{
			if (s[i + 1] == ' ' || s[i + 1] == '\t' || s[i + 1] == '\0')
				word++;
		}
		i++;
	}
	return (word);
}
